#include <unistd.h>

int	main(int c, char **v)
{
	int	a;
	int	i;

	a = 1;
	while (a < c)
	{
		i = 0;
		while (v[a][i] != 0)
		{
			write(1, &v[a][i], 1);
			i++;
		}
		write(1, "\n", 1);
		a++;
	}
}
