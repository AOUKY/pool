#include <unistd.h>

int	main(int c, char **v)
{
	int	i;
	int	a;

	a = c - 1;
	while (a > 0)
	{
		i = 0;
		while (v[a][i] != 0)
		{
			write(1, &v[a][i], 1);
			i++;
		}
		write(1, "\n", 1);
		a--;
	}
}
