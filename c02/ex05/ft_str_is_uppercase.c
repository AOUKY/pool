int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	x;

	i = 0;
	if (str[i] == '\0')
		x = 1;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z'))
		{
			x = 1;
		}
		else
		{
			x = 0;
			return (x);
		}
		i++;
	}
	return (x);
}
