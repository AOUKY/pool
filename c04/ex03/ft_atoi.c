int	ft_atoi(char *str)
{
	int	i;
	int	ng;
	int	nb;

	i = 0;
	nb = 0;
	ng = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			ng++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10;
		nb = nb + (str[i] - 48);
		i++;
	}
	if (ng % 2 == 0)
		return (nb);
	else 
		return (-nb);
}
